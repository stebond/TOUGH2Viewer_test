    private void jCheckBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox2ActionPerformed
        DisplayRenderer dRenderer = IsoS3DParent.display.getDisplayRenderer();
        try {
            GraphicsModeControl dispGMC = (GraphicsModeControl) IsoS3DParent.display.getGraphicsModeControl();
            dispGMC.setScaleEnable(true);
            if (jCheckBox2.isSelected()) {
                dispGMC.setProjectionPolicy(DisplayImplJ3D.PERSPECTIVE_PROJECTION);
            } else {
                dispGMC.setProjectionPolicy(DisplayImplJ3D.PARALLEL_PROJECTION);
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_jCheckBox2ActionPerformed

