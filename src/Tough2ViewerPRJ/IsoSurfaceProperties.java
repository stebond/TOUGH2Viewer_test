    private void jButtonBackGroundControlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackGroundControlActionPerformed
        Color c = JColorChooser.showDialog(IsoSurfaceProperties.this, "Choose a color...", getBackground());
        if (c != null) {
            float r = (float) c.getRed();
            float g = (float) c.getGreen();
            float b = (float) c.getBlue();
            r = r / 255.0f;
            g = g / 255.0f;
            b = b / 255.0f;
            Color1 = new Color3f(r, g, b);

            jTextField3.setForeground(c);
            jTextField3.setBackground(c);
            int i = 1;
            try {
                IsoS3DParent.change_background(Color1);
            } catch (Exception e) {

            }

        } else {

        }

    }//GEN-LAST:event_jButtonBackGroundControlActionPerformed

